package cn.damei.entity.mobile.Manager;

public class MainInterfaceEntity {
	
	private String key                     ;
	private String orderNumber             ;
	private String acceptanceDate          ;
	private String mainInstallItemName     ;
	private String secondCateCodeList;
	private String projectManagerName;
	private String projectManagerMobile;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}
	
	public String getAcceptanceDate() {
		return acceptanceDate;
	}
	public void setAcceptanceDate(String acceptanceDate) {
		this.acceptanceDate = acceptanceDate;
	}
	public String getMainInstallItemName() {
		return mainInstallItemName;
	}
	public void setMainInstallItemName(String mainInstallItemName) {
		this.mainInstallItemName = mainInstallItemName;
	}
	public String getSecondCateCodeList() {
		return secondCateCodeList;
	}
	public void setSecondCateCodeList(String secondCateCodeList) {
		this.secondCateCodeList = secondCateCodeList;
	}
	public String getProjectManagerName() {
		return projectManagerName;
	}
	public void setProjectManagerName(String projectManagerName) {
		this.projectManagerName = projectManagerName;
	}
	public String getProjectManagerMobile() {
		return projectManagerMobile;
	}
	public void setProjectManagerMobile(String projectManagerMobile) {
		this.projectManagerMobile = projectManagerMobile;
	}

	
}
